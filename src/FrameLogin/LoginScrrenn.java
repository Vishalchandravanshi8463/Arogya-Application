    private boolean validInputs() {
        getInput();
        if (LoginId.isEmpty() || pwd.length == 0 || userType == null) {
            return true;
        }
        return false;
    }

    private void getInput() {
        LoginId = txtLoginId.getText();
        pwd = txtPass.getPassword();
        userType = getUserTypes();
    }

    private String getUserTypes() {
        if (jrbAdmin.isSelected()) {
            return "Admin";
        } else if (jrbReceptionist.isSelected()) {
            return "RECEPTIONIST";
        } else if (jrbDoctor.isSelected()) {
            return "DOCTOR";
        } else {
            return null;
        }
    }

