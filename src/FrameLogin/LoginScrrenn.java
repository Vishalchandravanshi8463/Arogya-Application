    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        /*
        boolean isInputValid=validateInputs();
        
         */
        boolean isInputValid = validInputs();
        
        if (isInputValid) {
            JOptionPane.showMessageDialog(null, "Please input id and password  ", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String userType = getUserTypes();
        if (userType == null) {
            JOptionPane.showMessageDialog(null, "Please select a user type");
            return;
        }

        try {
            User user = new User();
            user.setUserId(LoginId);
            String pwdd = new String(PasswordEncryption.getEncryption(pwd));
            user.setPassword(pwdd);
            user.setUserType(userType);
            String userName = UserDao.validateUser(user);
            System.out.println(userName);
            
            if (userName != null) {
                JOptionPane.showMessageDialog(null, "Welcome " + userName, "Login success!", JOptionPane.INFORMATION_MESSAGE);
                UserProfile.setUserName(userName);
                
                UserProfile.setUserType(userType);
                UserProfile.setUserId(LoginId);//setUserId
                JFrame option = null;
                if (userType.equalsIgnoreCase("Admin")) {
                    option = new AdminOptionFrameJava();
                } else if (userType.equalsIgnoreCase("Receptionist")) {
                        option=new  ReceptionistOptionFrame();
                } else {
                        option=new DoctorOptionScreen();
                }
                option.setVisible(true);
                this.dispose();
                return;

            }
            JOptionPane.showMessageDialog(null, "Invalid credentials!", "Login denied!", JOptionPane.ERROR_MESSAGE);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Problem in DB ", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

