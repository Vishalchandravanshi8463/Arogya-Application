    private void loadDoctorId() {
        try {
            doctorIds = DoctorsDao.getAllDoctorId();
            for (String id : doctorIds) {
                jcmbDocId.addItem(id);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error in DB ");
            e.printStackTrace();
        }

    }

    private void getnewPatientId() {

        try {
            patientId = PatientDao.getNewPatientId();
            txtPatientId.setText(patientId);
            Date today = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
            String dateStr = sdf.format(today);
            txtDate.setText(dateStr);

            //take string return date 
            java.util.Date d1 = sdf.parse(dateStr);
            long ms = d1.getTime();
            dt = new java.sql.Date(ms);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error in DB");
            e.printStackTrace();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Error in Date Conversion ");
            ex.printStackTrace();
        }

    }

    private boolean validateInputs() {

        patient = new PatientPojo();
        patient.setPatient_Id(txtPatientId.getText().trim());
        patient.setCity(txtCity.getText().trim());
        patient.setDoctor_id(jcmbDocId.getSelectedItem().toString());
        patient.setMobile_no(txtPhoneNo.getText().trim());
        patient.setAddress(txtAddress.getText().trim());
        patient.setFirst_name(txtName.getText().trim());
        patient.setLast_name(txtLastName.getText().trim());
        patient.setM_status(jcmMStatus.getSelectedItem().toString());
        patient.setGender(jcmbGender.getSelectedItem().toString());
        patient.setP_date(dt);

        if (patient.getCity().isEmpty() || patient.getFirst_name().isEmpty() || patient.getLast_name().isEmpty() || patient.getAddress().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Please fill all the fields");
            return false;
        }
        try {
            patient.setAge(Integer.parseInt(txtAge.getText().trim()));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Age " + e);
            return false;
        }
        if (!isContactValid()) {
            JOptionPane.showMessageDialog(null, "Mobile number is Invalid ");
            return false;
        }
            return true;
    }

    private boolean isContactValid() {

        char[] mob = patient.getMobile_no().toCharArray();
        for (char ch : mob) {
            if (Character.isDigit(ch) == false) {
                return false;
            }

        }
        if (patient.getMobile_no().length() == 10) {
            return true;
        }
        return false;

    }
