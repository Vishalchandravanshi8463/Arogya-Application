    private void loadAllPatientsDetail() {

        try {
            List<PatientPojo> listt = PatientDao.getAllPatientDetails();
            if (listt.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No patients registered!");
                return;
            }
            Object obj[] = new Object[12];

            for (PatientPojo pa : listt) {
                obj[0] = pa.getPatient_Id();
                obj[1] = pa.getFirst_name();
                obj[2] = pa.getLast_name();
                obj[3] = pa.getAge();
                obj[4] = pa.getOpd();
                obj[5] = pa.getGender();
                obj[6] = pa.getM_status();
                obj[7] = pa.getP_date();
                obj[8] = pa.getAddress();
                obj[9] = pa.getCity();
                obj[10] = pa.getMobile_no();
                obj[11] = pa.getDoctor_id();

                model.addRow(obj);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in DB in Patient Details Frame ");
            e.printStackTrace();
        }

    }
