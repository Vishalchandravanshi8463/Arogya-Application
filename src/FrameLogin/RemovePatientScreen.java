    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String patient = cmbPatientId.getSelectedItem().toString();
        try {
            boolean result = PatientDao.deletePatientById(patient);
            boolean result1=AppointmentDao.deletePatientById(patient);
            if (result&&result1) {
                JOptionPane.showMessageDialog(null, "Patient removed successfully ");
                cmbPatientId.removeItem(patient);

                if (cmbPatientId.getItemCount() == 0) {
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null, " All Patient removed ");
                    ReceptionistOptionFrame fr = new ReceptionistOptionFrame();
                    fr.setVisible(true);
                    this.dispose();
                    return;
                }

            } else {
                JOptionPane.showMessageDialog(null, " Deletion Unsuccessful");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error in DB " + e);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

