    private void loadPatientID() {

        try {
            List<String> list = PatientDao.getAllPatientId();
            for (String s : list) {
                cmbPatientId.addItem(s);
            }
            if (cmbPatientId.getItemCount() != 0) {
                btnDelete.setEnabled(true);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in DB " + e);
            e.printStackTrace();
        }

    }
