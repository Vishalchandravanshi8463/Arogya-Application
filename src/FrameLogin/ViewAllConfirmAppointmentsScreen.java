    private void loadCongirmAppointmentDetails() {

        try
        {
            String docName = UserProfile.getUserName();
            appList = AppointmentDao.getAllConfirmAppointmentsByDoctorName(docName);
            if (appList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No appointment is Confirmed ");
                return;
            }
            Object []obj = new Object[4];
            for (AppointmentPojo app : appList) {
                obj[0] = app.getPatientId();
                obj[1] = app.getPatientName();
                obj[2] = app.getOpd();
                obj[3] = app.getAppointmentDate();

                model.addRow(obj);
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in DB ");
            e.printStackTrace();
        }
    }
