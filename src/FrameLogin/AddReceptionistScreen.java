    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if (validInput() == false) {
            JOptionPane.showMessageDialog(null, "Please input all fields ");
            return;
        }
        String result = matchPasswords();
        if (result != null) {
            JOptionPane.showMessageDialog(null, result);
            return;
        }
        try {
            UserPojo user = new UserPojo();
            user.setLoginId(loginId);
            String encPassword = new String(PasswordEncryption.getEncryption(password.toCharArray()));
            user.setPassword(encPassword);
            user.setUserType("RECEPTIONIST");
            user.setUserName(selEmpName);

            ReceptionistPojo recp = new ReceptionistPojo();
            recp.setReceptionistId(txtRecpID.getText());
            recp.setGender(jcmbGender.getSelectedItem().toString());
            recp.setReceptionistName(selEmpName);

            boolean recp1 = UserDao.addUser(user);
            boolean recp2 = ReceptionistDao.addRec(recp);

            if (recp1 == true && recp2 == true) {
                JOptionPane.showMessageDialog(null, "Receptionist registered Successfully ");
                clearAll();
                getNewRecId();
                return;
            }
            JOptionPane.showMessageDialog(null,"Receptionist register unsuccessful");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in DB in Regi Action Perform in Receptionist:" + e);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

