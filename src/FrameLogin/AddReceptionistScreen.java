    private void loadRecDetails() {

        try {
            unRegReceptionistList = EmpDao.getUnRegisteredReceptionist();
            Set<String> RecIdSet = unRegReceptionistList.keySet();
            for (String id : RecIdSet) {
                jcmEmpID.addItem(id);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error in DB in AddRecFrame  " + e);
            e.printStackTrace();
        }
    }

    private void getNewRecId() {

        try {
            String recId = ReceptionistDao.getNewRecID();
            txtRecpID.setText(recId);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in DB in new Rec Id " + e);
            e.printStackTrace();
        }
    }

    private boolean validInput() {

        loginId = txtLoginID.getText().trim();
        char[] pw1 = this.txtPassword.getPassword();
        char[] pw2 = this.txtRetypePassword.getPassword();

        if (loginId.isEmpty() || pw1.length == 0 || pw2.length == 0) {
            return false;
        }
        password = new String(pw1).trim();
        rePassword = new String(pw2).trim();
        return true;

    }

    private String matchPasswords() {

        if (password.length() < 3 || rePassword.length() < 3) {
            return "Password must be at least 3 characters long ";
        }
        if (password.equals(rePassword) == false) {
            return "Password do not match";
        }
        return null;
    }

    private void clearAll() {

        txtLoginID.setText("");
        txtPassword.setText("");
        txtRetypePassword.setText("");

        unRegReceptionistList.remove(selEmpId);
        jcmEmpID.removeItem(selEmpId);

        if (unRegReceptionistList.size() == 0) {
            JOptionPane.showMessageDialog(null, "All Receptionist Registered ");
            ManageReceptionist fr = new ManageReceptionist();
            fr.setVisible(true);
            this.dispose();
        }

    }
