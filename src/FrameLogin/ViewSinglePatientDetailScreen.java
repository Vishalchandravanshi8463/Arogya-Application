    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        patientID = jcmbPatient.getSelectedItem().toString();
        try {
            PatientPojo pat = new PatientPojo();
            pat = PatientDao.getPatientDetailById(patientID);
            txtOPD.setText(pat.getOpd());
            txtDoctorID.setText(pat.getDoctor_id());
            int age = pat.getAge();
            txtAge.setText(String.valueOf(age));
            txtCity.setText(pat.getCity());
            txtAddress.setText(pat.getAddress());
            txtName.setText(pat.getFirst_name());
            txtLastName.setText(pat.getLast_name());
            txtGender.setText(pat.getGender());

            Date today = new Date();
            today = pat.getP_date();
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
            String dateStr = sdf.format(today);

            txtDate.setText(dateStr);
            txtPhoneNo.setText(pat.getMobile_no());
            txtMarialStatus.setText(pat.getM_status());
            System.out.println("m status is " + pat.getM_status());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB");
            ex.printStackTrace();
        }


    }//GEN-LAST:event_btnSearchActionPerformed

