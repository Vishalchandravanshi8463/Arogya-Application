    private void loadEmpId() {

        try {
            List<String> listt = EmpDao.getAllemployeeId();
            for (String eid : listt) {
                jcmbEmpId.addItem(eid);
            }
            if (jcmbEmpId.getItemCount() != 0) {
                btnDelete.setEnabled(true);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in DB in Delete Emp Frame " + e);
        }
    }
