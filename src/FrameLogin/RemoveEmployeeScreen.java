    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id = jcmbEmpId.getSelectedItem().toString();
        try {
            boolean result = EmpDao.deleteEmpById(id);
            if (result) {
                JOptionPane.showMessageDialog(null, "Employee removed Successfully ");

                jcmbEmpId.removeItem(id);
                if (jcmbEmpId.getItemCount() == 0) {
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null, " All Doctors removed ");
                }
            } else {
                JOptionPane.showMessageDialog(null, " Deletion Unsuccessful");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error in DB " + e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

